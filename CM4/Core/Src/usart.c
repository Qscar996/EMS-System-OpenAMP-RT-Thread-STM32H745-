void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size)
{
	if(huart->Instance == USART3)
	{
		if(Size > RX_SIZE_MQTT)
		{
			Size = RX_SIZE_MQTT - 1;
		}
		//接收到新数据，记录长度并设置标志
		rx_dmabuffer[Size] = '\0';
		rx_dmadata_len = Size;

		//将数据拷贝到处理缓冲区，防止被下一次DMA接收冲刷掉
		memcpy(dma_process_buffer, rx_dmabuffer, Size);
		newdam_msg_flag = 1;
		HAL_UARTEx_ReceiveToIdle_DMA(&huart3, rx_dmabuffer, sizeof(rx_dmabuffer)-1);
	}
}

