/*MQTT指令应答*/
int AT_WaitResp(char *expected, uint32_t timeout_ms)
{
    char buf[128];          // 响应接收缓冲区，大小可根据需要调整
    uint16_t idx = 0;
    uint32_t start_tick = HAL_GetTick();
    uint8_t ch;

    while ((HAL_GetTick() - start_tick) < timeout_ms)
    {
        // 每次尝试接收一个字节，超时 10ms 以便及时检查总超时
        if (HAL_UART_Receive(&huart3, &ch, 1, 10) == HAL_OK)
        {
            if (ch == '\n')          // 遇到换行符，表示一行结束
            {
                buf[idx] = '\0';     // 字符串终结
                if (strstr(buf, expected) != NULL)
                {
                    return 1;        // 匹配成功
                }
                idx = 0;             // 重置缓冲区，准备接收下一行
            }
            else
            {
                // 忽略回车符 '\r'，避免干扰匹配
                if (ch != '\r')
                {
                    if (idx < sizeof(buf) - 1)
                    {
                        buf[idx++] = ch;
                    }
                    else
                    {
                        // 缓冲区溢出保护：清空并重新开始（避免截断匹配）
                        idx = 0;
                    }
                }
            }
        }
        // 若接收超时（10ms 内无数据），继续循环，总超时由 while 条件控制
    }
    return 0;   // 超时未匹配
}

